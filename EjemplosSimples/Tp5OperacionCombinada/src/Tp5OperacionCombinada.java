public class Tp5OperacionCombinada {

    public static void main(String[] args) {
        // Operacion combinada
        double resultado,a,b,c,d,e,f;
        a=6.0/5.0;
        b=2.0/3.0;
        c=7.0/2.0;
        d=2.0/30.0;
        e=1.0/3.0;
        f=5.0;
        resultado = (a-b*c+d)/(e/f);

        System.out.println("resultado = " + resultado);
    }
}
